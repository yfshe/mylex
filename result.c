void yy_create_buffer(){
	if (yy_in == NULL){
		yy_in = stdin;
		yy_eof = '\n';
	}
	int size = 10;
	yy_buffer = malloc(size * sizeof(char));
	int i = 0;
	char c;
	while((c = fgetc(yy_in)) != yy_eof){
		yy_buffer[i++] = c;
		if (i == size-1){
			char *new_buffer = (char *)malloc(size * 2 * sizeof(char));
			int j;
			for(j = 0; j < size; j++)
				new_buffer[j] = yy_buffer[j];
			free(yy_buffer);
			yy_buffer = new_buffer;
			size *= 2;
		}
	}
	yy_buffer[i] = '\0';
	yy_cp = yy_buffer;
}

